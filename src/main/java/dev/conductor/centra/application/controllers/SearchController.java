    // I assume that classes generated by antlr aren't thread safe.
    // Hence need separate harness for each GET request
    private CqlCustomListener setup(String cqlString) {
        CodePointCharStream input = CharStreams.fromString(cqlString);
        cqlLexer lexer = new cqlLexer(input);
        CommonTokenStream tokens = new CommonTokenStream(lexer);
        cqlParser parser = new cqlParser(tokens);
        ParseTree tree = parser.cql_stmt_list();

        ParseTreeWalker walker = new ParseTreeWalker();
        CqlCustomListener listener = new CqlCustomListener();
        walker.walk(listener, tree);
        return  listener;
    }
